void AnimeObject::move() {
	velY -= 1;
	x += velX;
	y += velY;
	if (x + width / 4 >= 2 * WIDTH - CAMERAW)
		x = WIDTH + width / 4;
	if (y + height / 4 >= 2 * HEIGHT + CAMERAH)
		y = HEIGHT + height / 4;
	if (x < 0)
		x = 0;
	if (y < 0)
		y = 0;
}
